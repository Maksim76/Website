function translatePage(lang) {
    const translationsForLang = translations[lang] || translations.az;
    document.documentElement.lang = lang === 'az' ? 'az' : lang;
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.dataset.i18n;
        const text = translationsForLang[key];
        if (text !== undefined) {
            if (key === 'heroHeading') {
                el.innerHTML = text;
            } else {
                el.textContent = text;
            }
        }
    });
    document.querySelectorAll('.lang-option').forEach(button => {
        button.classList.toggle('active', button.dataset.lang === lang);
    });

    document.querySelectorAll('.lang-toggle').forEach(toggle => {
        toggle.innerHTML = `${lang.toUpperCase()} <i class="fas fa-chevron-down"></i>`;
        toggle.setAttribute('aria-expanded', 'false');
    });

    document.querySelectorAll('.lang-dropdown').forEach(dropdown => {
        dropdown.classList.remove('open');
    });

    currentLanguage = lang;
    renderMenuItems(currentMenuCategory);
    localStorage.setItem('siteLanguage', lang);
}

function renderMenuItems(category = currentMenuCategory) {
    const container = document.querySelector('.menu-grid');
    if (!container) return;

    const translationsForLang = translations[currentLanguage] || translations.az;
    const items = menuItemsByCategory[category] || [];
    container.innerHTML = '';

    items.forEach(item => {
        const card = document.createElement('div');
        card.className = 'menu-card';

        const image = document.createElement('img');
        image.className = 'menu-card-image';
        image.src = item.image;
        image.alt = translationsForLang[item.nameKey] || '';

        const body = document.createElement('div');
        body.className = 'menu-card-body';

        const title = document.createElement('h3');
        title.textContent = translationsForLang[item.nameKey] || '';

        const description = document.createElement('p');
        description.textContent = translationsForLang[item.descKey] || '';

        const footer = document.createElement('div');
        footer.className = 'menu-card-footer';

        const price = document.createElement('span');
        price.textContent = item.price;

        body.append(title, description, footer);
        footer.appendChild(price);
        card.append(image, body);
        container.appendChild(card);
    });
}

function setActiveMenuCategory(category) {
    currentMenuCategory = category;
    document.querySelectorAll('.menu-category').forEach(button => {
        button.classList.toggle('active', button.dataset.category === category);
    });
    renderMenuItems(category);
}

function initMenuCategories() {
    document.querySelectorAll('.menu-category').forEach(button => {
        button.addEventListener('click', () => {
            setActiveMenuCategory(button.dataset.category);
        });
    });
    setActiveMenuCategory(currentMenuCategory);
}

function initLanguageSwitcher() {
    document.querySelectorAll('.lang-option').forEach(option => {
        option.addEventListener('click', event => {
            event.stopPropagation();
            translatePage(option.dataset.lang);
        });
    });

    document.querySelectorAll('.lang-toggle').forEach(toggle => {
        toggle.addEventListener('click', event => {
            event.stopPropagation();
            const dropdown = toggle.closest('.lang-dropdown');
            const isOpen = dropdown.classList.toggle('open');
            toggle.setAttribute('aria-expanded', String(isOpen));
        });
    });

    document.addEventListener('click', () => {
        document.querySelectorAll('.lang-dropdown.open').forEach(dropdown => {
            dropdown.classList.remove('open');
            const toggle = dropdown.querySelector('.lang-toggle');
            if (toggle) {
                toggle.setAttribute('aria-expanded', 'false');
            }
        });
    });

    const savedLang = localStorage.getItem('siteLanguage') || 'az';
    translatePage(savedLang);
    
    // Восстанавливаем сохраненную страницу
    const savedPage = localStorage.getItem('currentPage') || 'home';
    showPage(savedPage);
    initMenuCategories();
}

// Плавная прокрутка к секциям
document.querySelectorAll('nav a').forEach(anchor => {
    anchor.addEventListener('click', function (e) {
        e.preventDefault();
        const href = this.getAttribute('href');
        if (href.startsWith('#')) {
            const target = document.querySelector(href);
            if (target) {
                target.scrollIntoView({
                    behavior: 'smooth'
                });
            }
        } else {
            showPage(href.substring(1));
        }
    });
});

// Функция для переключения страниц
function showPage(pageId) {
    document.querySelectorAll('.page').forEach(page => {
        page.classList.remove('active');
    });
    const targetPage = document.getElementById(pageId) || document.querySelector('.hero');
    if (targetPage) {
        targetPage.classList.add('active');
    }
    // Сохраняем текущую страницу в localStorage
    localStorage.setItem('currentPage', pageId);
}

// Активные вкладки заказа
const orderTabs = document.querySelectorAll('.order-tabs .tab');
orderTabs.forEach(tab => {
    tab.addEventListener('click', () => {
        orderTabs.forEach(item => item.classList.remove('active'));
        tab.classList.add('active');
    });
});

initLanguageSwitcher();
